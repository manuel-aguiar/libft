#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned char	cc;

	cc = (unsigned char)c;
	while (*s && *s != cc)
		s++;
	if (*s || cc == '\0')
		return ((char *)s);
	return (NULL);
}
